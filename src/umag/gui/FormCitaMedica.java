    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        
        int id = Integer.parseInt(txtID.getText());
        int rango = Integer.parseInt((String)cmbRango.getSelectedItem());
        boolean beneficiario = rdbBeneficiarioSi.isSelected();
        boolean especialista = chkboxSi.isSelected();
        String tipoAfiliacion = (String)cmbAfiliacion.getSelectedItem();
        float resultado = 0;
        
        if (rango == 1) {
            resultado = 2_500f;
        
        } else if (rango == 2) {
            resultado = 17_000f;
        
        } else if (rango == 3){
            resultado = 50_000f;
        }
        
        if (beneficiario) {
            resultado = resultado + resultado*0.2f;
        }
        
        if (especialista) {
            resultado = resultado + 20_000f;
        }
        
        if (tipoAfiliacion.equalsIgnoreCase("Complementaria")) {
            resultado = 0.0f;
        
        } else if (tipoAfiliacion.equalsIgnoreCase("Prepagada")){
            resultado = (float)resultado/2;
        }
        txtResultado.setText(Float.toString(resultado));
        
        Cita cita = new Cita(id, especialista, beneficiario, tipoAfiliacion, resultado);
        citas.add(cita);
    }//GEN-LAST:event_btnCalcularActionPerformed

